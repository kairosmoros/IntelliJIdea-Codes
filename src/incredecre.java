class  incredecre
{
    public static void main(String[] args)
    {
        int a=10;
        int b=5;
        System.out.println(a);//10
        a++;//post increment
        System.out.println(a);//11
        ++a;//pre increment
        System.out.println(a);//12
        --a;//pre decrement
        System.out.println(a);//11
        a--;//post decrement
        System.out.println(b);//5
        int d=90;
        int k=2;
        System.out.println(d--);//90
        System.out.println(d);//89
        System.out.println(d-- + k);//91
    }
}
